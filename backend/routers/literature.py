@router.post("/summarize_paper", response_model=PaperSummary)
async def summarize_paper_endpoint(payload: SummarizePaperRequest, user=Depends(get_current_user)) -> PaperSummary:
    """
    Generate a detailed reviewer-grade analysis for a single paper.
    Fetches full text (PMC XML > OA PDF > Sci-Hub > abstract) then runs the LLM template.
    """
    provider = await _get_provider_for_user(user["id"])
    if not provider:
        raise HTTPException(status_code=400, detail="No AI provider configured. Open Settings to add one.")
    project_id = payload.project_id or payload.session_id
    project_folder: str | None = None
    if project_id:
        project = await load_session(user["id"], project_id)
        if project is not None:
            project_folder = project.get("project_folder")
    fs = await _fetch_settings_for_user(user["id"], project_folder)
    async with TokenContext(project_id=project_id, user_id=user["id"], stage="summarize_paper"):
        return await summarize_paper(
            provider, payload.paper, payload.query,
            fetch_settings=fs, session_id=project_id,
        )


# ── Critical summary ───────────────────────────────────────────────────────────

@router.post("/generate_summary", response_model=CriticalSummary)
async def generate_summary(payload: SummaryRequest, user=Depends(get_current_user)) -> CriticalSummary:
    """
    Tries AI summary first (OpenAI); falls back to heuristic extraction.
    """
    provider = await _get_provider_for_user(user["id"])
    if provider and payload.papers:
        try:
            return await _ai_summary(provider, payload.papers, payload.key_idea or "")
        except Exception as exc:
            logger.warning("AI summary failed (%s), falling back to heuristic.", exc)
    return _heuristic_summary(payload.papers, payload.key_idea or "")


# ── AI summary (OpenAI) ────────────────────────────────────────────────────────

_SUMMARY_SYSTEM = """\
You are a senior academic researcher conducting a systematic literature review.
Analyse the provided research papers and return a structured critical summary as JSON.

Return ONLY valid JSON in this exact shape — no markdown, no extra text:
{
  "core_points": [
    "Specific, evidence-backed statement drawn from the literature (4-6 items)"
  ],
  "new_data_explained": [
    "Concrete empirical finding, statistic, or novel result cited in the abstracts (4-6 items)"
  ],
  "cross_references": [
    {
      "paper_title": "Exact paper title",
      "doi": "DOI string or null",
      "relevance": "1-2 sentences explaining why this paper is central to the research question"
    }
  ]
}

Rules:
- Be specific — cite actual numbers, effect sizes, p-values, or percentages where mentioned.
- Do NOT fabricate data not present in the abstracts.
- Cross-reference all papers provided.
"""


async def _ai_summary(provider: AIProvider, papers: list[Paper], key_idea: str) -> CriticalSummary:
    blocks = []
    for i, p in enumerate(papers[:25], 1):  # cap at 25 to stay within context limits
        block = f"[{i}] {p.title} ({p.year or 'n.d.'})\n"
        block += f"Authors: {', '.join(p.authors[:3])}\n"
        if p.journal:
            block += f"Journal: {p.journal}\n"
        if p.citation_count is not None:
            block += f"Citations: {p.citation_count:,}\n"
        if p.abstract:
            block += f"Abstract: {p.abstract[:900]}\n"
        blocks.append(block)

    user_msg = (
        f"Research question: {key_idea}\n\n"
        f"Papers ({len(blocks)} of {len(papers)}):\n\n"
        + "\n---\n".join(blocks)
    )

    raw = await provider.complete(
        system=_SUMMARY_SYSTEM,
        user=user_msg,
        json_mode=True,
        temperature=0.2,
    )
    data = json.loads(raw)
    return CriticalSummary(
        core_points=data.get("core_points") or [],
        new_data_explained=data.get("new_data_explained") or [],
        cross_references=[
            CitationPoint(**ref)
            for ref in (data.get("cross_references") or [])
            if isinstance(ref, dict)
        ],
    )


# ── Cross-reference pipeline ───────────────────────────────────────────────────

@router.post("/sessions/{session_id}/stream_cross_references")
async def stream_cross_references_endpoint(
    session_id: str,
    payload: CrossReferenceRequest,
    user=Depends(get_current_user),
) -> StreamingResponse:
    """
    SSE endpoint that fetches and summarizes papers cited by the session's primary papers.

    Depth 1: papers cited by primary (depth=0) papers
    Depth 2: additionally papers cited by those depth-1 papers

    Event types: start, resolving, paper_done, skip, depth_complete, complete, warning, error
    """
    provider = await _get_provider_for_user(user["id"])
    if not provider:
        raise HTTPException(status_code=400, detail="No AI provider configured. Open Settings to add one.")

    fs = await _fetch_settings_for_user(user["id"])
    session = await load_session(user["id"], session_id)
    if session is None:
        raise HTTPException(status_code=404, detail="Session not found.")
    session_query = str(session.get("query") or "")

    async def generate():
        try:
            from services.cross_reference_engine import stream_cross_references
            async with TokenContext(project_id=session_id, user_id=user["id"], stage="cross_reference"):
                async for event in stream_cross_references(
                    session_id=session_id,
                    depth=payload.depth,
                    provider=provider,
                    query=session_query,
                    fetch_settings=fs,
                    purpose_filter=payload.purpose_filter or [],
                ):
                    yield _sse(event)
        except Exception as exc:
            logger.exception("stream_cross_references fatal error")
            yield _sse({"type": "error", "message": str(exc)})

    return StreamingResponse(
        generate(),
        media_type="text/event-stream",
        headers={
            "Cache-Control": "no-cache",
            "X-Accel-Buffering": "no",
            "Connection": "keep-alive",
        },
    )


