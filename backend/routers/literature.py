# ── Heuristic fallback summary ─────────────────────────────────────────────────

def _heuristic_summary(papers: list[Paper], key_idea: str) -> CriticalSummary:
    EMPIRICAL = {
        "found", "showed", "demonstrated", "reported", "identified",
        "revealed", "observed", "indicated", "confirmed", "established",
        "suggests", "suggested",
    }
    core: list[str] = []
    new_data: list[str] = []
    refs: list[CitationPoint] = []

    for p in papers:
        abstract = (p.abstract or "").strip().replace("\n", " ")
        if abstract:
            sentences = [s.strip() for s in abstract.split(". ") if len(s.strip()) > 20]
            if sentences:
                core.append(sentences[0])
            for s in sentences[1:]:
                if any(kw in s.lower().split() for kw in EMPIRICAL):
                    new_data.append(s)
                    break
        cite = (
            f"Cited by {p.citation_count:,} works."
            if p.citation_count is not None
            else "Citation count unavailable."
        )
        refs.append(CitationPoint(
            paper_title=p.title,
            doi=p.doi,
            relevance=f"[{p.source.upper()}] {p.year or 'n.d.'} · {cite}",
        ))

    return CriticalSummary(
        core_points=list(dict.fromkeys(core))[:6],
        new_data_explained=list(dict.fromkeys(new_data))[:6],
        cross_references=refs,
    )
